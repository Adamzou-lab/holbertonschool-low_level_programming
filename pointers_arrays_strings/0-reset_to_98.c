/**
 * reset_to_98 - Updates the value pointed to by a pointer to 98.
 * @n: Pointer to an integer to be updated.
 *
 * Description: Takes a pointer to an int as a parameter
 * and updates the value it points to to 98.
 *
 * Return: Nothing.
 */
